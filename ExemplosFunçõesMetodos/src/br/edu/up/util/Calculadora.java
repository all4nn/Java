package br.edu.up.util;

public class Calculadora {
	
	public double somar(double a, double b) {
		return a + b;
	}
	public double  sub(double a , double b){
		return a - b;
	}
	public double  multi(double a , double b){
		return a * b;
	}
	public double  divi(double a , double b){
		return a / b;
	}

}
 